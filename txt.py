import turtle
while True:
  for c in['red','turquoise','yellow','blue','green','black']:
    anomaly=turtle
    anomaly.color(c)
    anomaly.forward(90)
    anomaly.left(90)
    anomaly.backward(45)
    anomaly.left(90)
    anomaly.forward(90)
    anomaly.right(90)
    anomaly.forward(90)
    anomaly.left(60)